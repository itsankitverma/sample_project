    public boolean isModified() {
        if (depositSlipNumber == null) {
            if (depositSlipNbrPrev != null) return true;
        } else {
            if (!depositSlipNumber.equals(depositSlipNbrPrev)) return true;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");

        if ((depoDt == null && depoDtPrev != null) || (depoDt != null && depoDtPrev == null)) {
            return true;
        } else {
            if (depoDt != null && depoDtPrev != null) {
                if (!sdf.format(depoDt).equals(sdf.format(depoDtPrev))) return true;
            }
        }

        if ((bankDepoDt == null && bankDepoDtPrev != null) || (bankDepoDt != null && bankDepoDtPrev == null)) {
            return true;
        } else {
            if (bankDepoDt != null && bankDepoDtPrev != null) {
                if (!sdf.format(bankDepoDt).equals(sdf.format(bankDepoDtPrev))) return true;
            }
        }

        return (bankAccountCd != bankAccountCdPrev || actualAmt != actualAmtPrev || bankAmt != bankAmtPrev || statusId != statusIdPrev);

    }

