    public boolean isModified() {
        if (recOtherDocNumber == null) {
            if (recOtherDocNumberPrev != null) return true;
        } else {
            if (!recOtherDocNumber.equals(recOtherDocNumberPrev)) return true;
        }

        if (rcptNbr == null) {
            if (rcptNbrPrev != null) return true;
        } else {
            if (!rcptNbr.equals(rcptNbrPrev)) return true;
        }

        if (customerNm == null) {
            if (customerNmPrev != null) return true;
        } else {
            if (!customerNm.equals(customerNmPrev)) return true;
        }

        if (recComment == null) {
            if (recCommentPrev != null) return true;
        } else {
            if (!recComment.equals(recCommentPrev)) return true;
        }

        if (otherComment == null) {
            if (otherCommentPrev != null) return true;
        } else {
            if (!otherComment.equals(otherCommentPrev)) return true;
        }

        if (recvPrepyAmt == 0) {
            if (recvPrepyAmtPrev != 0) return true;
        } else {
            if (recvPrepyAmt != recvPrepyAmtPrev) return true;
        }

        if (billAccount == null){
        	if (billAccountPrev != null) return true;
        } else {
        	if (billAccount != billAccountPrev) return true;
        }

        return (recCashPayment != recCashPaymentPrev || otherPaymentType != otherPaymentTypePrev || recCheckPayment != recCheckPaymentPrev || recStatusCodePk != recStatusCodePkPrev || recvPrepyAmt != recvPrepyAmtPrev || recAmount != recAmtPrev);

    }

