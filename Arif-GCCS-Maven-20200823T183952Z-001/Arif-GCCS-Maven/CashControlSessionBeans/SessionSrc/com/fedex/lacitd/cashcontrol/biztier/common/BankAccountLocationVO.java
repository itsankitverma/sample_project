package com.fedex.lacitd.cashcontrol.biztier.common;

import com.fedex.lacitd.cashcontrol.datatier.valueobject.BankVO;

import java.util.Collection;
import java.util.HashMap;

/**
 * @author Arturo Gonzalez
 */
public class BankAccountLocationVO implements java.io.Serializable {

    private BankVO bank;
    private Collection bankAccount;
    private HashMap accountLocations;


    /**
     * @return Returns the bank.
     */
    public BankVO getBank() {
        return bank;
    }

    /**
     * @param bank The bank to set.
     */
    public void setBank(BankVO bank) {
        this.bank = bank;
    }

    /**
     * @return Returns the accountLocations.
     */
    public HashMap getAccountLocations() {
        return accountLocations;
    }

    /**
     * @param accountLocations The accountLocations to set.
     */
    public void setAccountLocations(HashMap accountLocations) {
        this.accountLocations = accountLocations;
    }

    /**
     * @return Returns the bankAccount.
     */
    public Collection getBankAccount() {
        return bankAccount;
    }

    /**
     * @param bankAccount The bankAccount to set.
     */
    public void setBankAccount(Collection bankAccount) {
        this.bankAccount = bankAccount;
    }
}
